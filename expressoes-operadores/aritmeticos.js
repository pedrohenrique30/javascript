// Operadores aritmeticos

let number1 = 14;
let number2 = 7;

// Soma +
console.log(number1 + number2)

// Subtração -
console.log(number1 - number2);

// Multiplicação *
console.log(number1 * number2);

// Divisão /
console.log(number1 / number2);

// Resto de uma divisão %
console.log(number1 % number2)